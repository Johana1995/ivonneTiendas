		function load(page){
			var q= $("#q").val();
			$("#loader").fadeIn('slow');
			$.ajax({
				url:'./assets/ajax/productos_factura.php?action=ajax&page='+page+'&q='+q,
				 beforeSend: function(objeto){
				 $('#loader').html('<img src="./img/ajax-loader.gif"> Cargando...');
			  },
				success:function(data){
					$(".outer_div").html(data).fadeIn('slow');
					$('#loader').html('');
					
				}
			})
		}

	function agregar (id)
		{
			var cantidadU=document.getElementById('cantidadUVenta_'+id).value;
			var cantidadP=document.getElementById('cantidadPVenta_'+id).value;
			//Inicia validacion
			alert('id'+id+'  u'+cantidadU+'  p='+cantidadP)
			if (isNaN(cantidadU))
			{
			alert('Esto no es un numero');
			document.getElementById('cantidad_'+id).focus();
			return false;
			}
			if (isNaN(cantidadP))
			{
			alert('Esto no es un numero');
			document.getElementById('precio_venta_'+id).focus();
			return false;
			}
			//Fin validacion
			
			$.ajax({
        type: "POST",
        url: "./assets/ajax/agregar_facturacion.php",
        data: "id="+id+"&cantidadU="+cantidadU+"&cantidadP="+cantidadP,
		 beforeSend: function(objeto){
			$("#resultados").html("Mensaje: Cargando...");
		  },
        success: function(datos){
		$("#resultados").html(datos);
		}
			});
		}
		
